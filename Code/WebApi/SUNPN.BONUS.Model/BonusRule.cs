using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace SUNPN.BONUS.Model
{

    public class BonusRule
    {
        /// <summary>
        /// 奖金规则Id
        /// </summary>
        private string bRId;
        public string BRId
        {
            get { return bRId; }
            set { bRId = value; }
        }

        /// <summary>
        /// 奖金规则名称
        /// </summary>
        private string bRName;
        public string BRName
        {
            get { return bRName; }
            set { bRName = value; }
        }

        /// <summary>
        /// 刨除人数
        /// </summary>
        private int remainNum;
        public int RemainNum
        {
            get { return remainNum; }
            set { remainNum = value; }
        }

        /// <summary>
        /// 受益人数
        /// </summary>
        private int gainNum;
        public int GainNum
        {
            get { return gainNum; }
            set { gainNum = value; }
        }

        /// <summary>
        /// 平均受益金额
        /// </summary>
        private decimal avgMoney;
        public decimal AvgMoney
        {
            get { return avgMoney; }
            set { avgMoney = value; }
        }

        /// <summary>
        /// 奖金项Id
        /// </summary>
        private string bonusItemId;
        public string BonusItemId
        {
            get { return bonusItemId; }
            set { bonusItemId = value; }
        }

        /// <summary>
        /// 设置规则的用户Id
        /// </summary>
        private string openId;
        public string OpenId
        {
            get { return openId; }
            set { openId = value; }
        }

        /// <summary>
        /// 奖金类型
        /// </summary>
        public string BonuType { get; set; }

        /// <summary>
        /// 受益部门
        /// </summary>
        public string DepId { get; set; }

        /// <summary>
        /// 公司Id
        /// </summary>
        public string CompanyId { get; set; }
    }
}
